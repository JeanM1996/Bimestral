      public void CargarComboCatalogo(){
          DefaultComboBoxModel cmbModel=new DefaultComboBoxModel();
          ArrayList<Administracion>lstCat=new ArrayList<>();
          lstCat=mp.ConsultarCatalogo2();
          for (Administracion adm:lstCat){
              Administracion objCatalogo= new Administracion(adm.getIdCatalogo(),adm.getStrDescripcion());
             cmbModel.addElement(objCatalogo);
             jComboBox1.setModel(cmbModel);
          }
          jComboBox1.insertItemAt("Seleccione",0);
          jComboBox1.setSelectedIndex(0);
          
      }
      
