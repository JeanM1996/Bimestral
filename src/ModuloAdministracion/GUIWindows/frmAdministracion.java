  public final void LlenarTabla() throws ClassNotFoundException, SQLException{
            //Como el mÃ©todo de la BL retorna un ArrayList de la clase Estudiantes
            //Aqui lo recuperamos
            ArrayList<Administracion> lstAdministracion = mp.ListaCatalogos();
           
            Object columnas[] = {"idCatalogo","Codigo Catalogo", "Nombre","Estado"};
            DefaultTableModel modelo = new DefaultTableModel(null, columnas);
            jTable1.setModel(modelo); 
            
            for (Administracion objeto : lstAdministracion)
            {
                //String id=String.valueOf(objeto.getStrCodigo());
                String idC=String.valueOf(objeto.getIdCatalogo());
                String estC=String.valueOf(objeto.getIntEstadoCatalogo());
                
                String modeloTemp[] = {idC,objeto.getStrCodCatalogo(),objeto.getStrNombreCatalogo(),estC};
                modelo.addRow(modeloTemp);
            }
 }
