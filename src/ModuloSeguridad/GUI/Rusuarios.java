  public final void LlenarTabla() throws ClassNotFoundException, SQLException{
            //Como el m칠todo de la BL retorna un ArrayList de la clase Estudiantes
            //Aqui lo recuperamos
            ArrayList<Usuario> lstusuarios = mp.ListaUsuarios();
           
            Object columnas[] = {"us_id","us_nombre", "us_apellido","us_login","us_password"};
            DefaultTableModel modelo = new DefaultTableModel(null, columnas);
            jTable3.setModel(modelo); 
            
            for (Usuario objeto : lstusuarios)
            {
                //String id=String.valueOf(objeto.getStrCodigo());
                String idC=String.valueOf(objeto.getIdUsuario());
                String nombre=String.valueOf(objeto.getNombreUsuario());
                String apellido=String.valueOf(objeto.getApellidoUsuario());
                String login=String.valueOf(objeto.getUsername());
                String strPasswordE=objeto.getPassword();
                String strPasswordD=Security.decodeWithAES(Security.key, strPasswordE);
                String modeloTemp[] = {idC,nombre,apellido,login, strPasswordD};
                modelo.addRow(modeloTemp);
            }
 }
