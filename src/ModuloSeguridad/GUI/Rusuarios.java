        public void ValidaryEscribir() throws ClassNotFoundException, SQLException{
        strDNI=txtDni.getText();
        strNombre=txtNombre.getText();
        strApellido=txtApellido.getText();
        strUsername=txtUsername.getText();
        strPassword= txtPassword.getText();
        if (!strNombre.equals("")&&!strApellido.equals("")&&!strUsername.equals("")&&!strPassword.equals("")) { 
            boolean encontrado=mp.validarUs(strDNI,strUsername);
            if (encontrado) {
                JOptionPane.showMessageDialog(rootPane ,"DNI ya Registrado","Advertencia",JOptionPane.ERROR_MESSAGE);
            }else{

                                  mp.InsertarUsuario(strDNI,strNombre.toUpperCase(),strApellido.toUpperCase(),strUsername,Security.encodeWithAES(Security.key,strPassword));   
                        JOptionPane.showMessageDialog(rootPane ,"Registrado con Exito","Informe",JOptionPane.INFORMATION_MESSAGE);   
   
            }
        }else{
             JOptionPane.showMessageDialog(rootPane ,"Algunos campos estan vacios\n Ingrese informacion en los campos faltantes","Advertencia",JOptionPane.ERROR_MESSAGE);
        }
        }  
